#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int	i;

	if (!s)
		return ;
	i = 0;
	while (s[i])
	{
		ft_putchar_fd(s[i], fd);
		if (s[i + 1] == '\0')
			ft_putchar_fd('\n', fd);
		i++;
	}
}
